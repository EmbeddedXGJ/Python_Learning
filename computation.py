a = 5
b = 2

print("加法运算",a + b)   #加法运算
print("\r\n")

print("减法运算",a - b)  #减法运算
print("\r\n")

print("乘法运算：",a * b)  #乘法运算
print("\r\n")

print("除法运算(小数)：",a / b)  #除法运算  有小数
print("\r\n")

print("除法运算(整除):",a // b) #除法运算  与C语言整数除法一致
print("\r\n")

print("取模运算:",a % b)   #取模运算
print("\r\n")

print("平方运算:",a ** b)  #平方运算 a^b
print("\r\n")
